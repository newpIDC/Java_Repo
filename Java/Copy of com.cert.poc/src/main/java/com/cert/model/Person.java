package com.cert.model;

public final class Person {
	private String _strName = "";
	private String _strWork = "";
	
	public String getName() {
		return _strName;
	}
	public void setName(final String strName) {
		_strName = strName;
	}
	public String getWork() {
		return _strWork;
	}
	public void setWork(final String strWork) {
		_strWork = strWork;
	}
}
